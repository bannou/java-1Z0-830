public class D01_MathMinMaxDemo {
    public static void main(String[] args) {
        // Integer operations
        int min = Math.min(5, 10);
        int max = Math.max(5, 10);
        
        System.out.println("Integer comparisons:");
        System.out.println("Math.min(5, 10) = " + min);
        System.out.println("Math.max(5, 10) = " + max);
        
        // Double operations
        double min2 = Math.min(5.7, 10.2);
        double max2 = Math.max(5.7, 10.2);
        
        System.out.println("\nDouble comparisons:");
        System.out.println("Math.min(5.7, 10.2) = " + min2);
        System.out.println("Math.max(5.7, 10.2) = " + max2);
    }
}
